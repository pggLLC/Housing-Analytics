/**
 * cross-county-disclosure.js
 *
 * Helper for surfacing a "this jurisdiction crosses county lines" warning
 * when the user is modeling a CO place that spans multiple counties.
 *
 * Why this matters
 * ----------------
 * HUD AMI is set at the county level (HMFA). 26 CO places — including
 * Aurora, Erie, Boulder, Longmont, Northglenn, Superior, Windsor, etc. —
 * physically straddle county boundaries. The Deal Calculator and PMA
 * simulator pick the place's *primary* county (highest population share)
 * for AMI lookups, but a parcel on the wrong side of the line uses a
 * different county's HUD AMI. The 2025 HAMFI delta between Boulder and
 * Weld is ~25%; for a 60% AMI deal, that's a 25% rent delta.
 *
 * Public API
 * ----------
 *   window.CrossCountyDisclosure.init() — fetch + cache the data file
 *   window.CrossCountyDisclosure.lookup(geoid) — returns null or:
 *     {
 *       name: "Aurora (city)",
 *       primary_county: "08005",
 *       all_counties: [{fips, name, population}, ...]
 *     }
 *   window.CrossCountyDisclosure.lookupByCountyFips(countyFips) — returns
 *     a list of cross-county places where this county is one of the
 *     touched counties (primary OR secondary). Useful for surfacing
 *     "If your site is in [X], confirm the parcel's actual county."
 *   window.CrossCountyDisclosure.formatBanner(place) — returns ready-to-
 *     render HTML for the disclosure banner.
 *
 * Data source
 * -----------
 *   data/hna/cross-county-places.json  (regenerated by
 *   scripts/hna/build_cross_county_places.py)
 */
(function () {
  'use strict';

  var DATA_URL = 'data/hna/cross-county-places.json';
  var _cache = null;
  var _byCounty = null;
  var _loadPromise = null;

  function _resolveDataUrl() {
    // Honor DataService.baseData if present (handles GitHub Pages prefix);
    // otherwise fall back to the relative URL.
    if (typeof window !== 'undefined' && window.DataService && typeof window.DataService.baseData === 'function') {
      return window.DataService.baseData(DATA_URL);
    }
    return DATA_URL;
  }

  function init() {
    if (_cache) return Promise.resolve(_cache);
    if (_loadPromise) return _loadPromise;
    var url = _resolveDataUrl();
    var loader = (typeof window !== 'undefined' && window.DataService && window.DataService.getJSON)
      ? window.DataService.getJSON(url)
      : fetch(url).then(function (r) { return r.json(); });
    _loadPromise = loader.then(function (doc) {
      _cache = doc || { places: {}, meta: {} };
      _byCounty = _buildCountyIndex(_cache.places || {});
      return _cache;
    }).catch(function (err) {
      // Soft-fail: missing data file is not fatal — the disclosure simply
      // won't surface. Log once for diagnosis.
      console.warn('[cross-county-disclosure] Could not load ' + url + ':', err);
      _cache = { places: {}, meta: {} };
      _byCounty = {};
      return _cache;
    });
    return _loadPromise;
  }

  function _buildCountyIndex(places) {
    var index = {};
    Object.keys(places).forEach(function (geoid) {
      var p = places[geoid];
      if (!p || !Array.isArray(p.all_counties)) return;
      p.all_counties.forEach(function (c) {
        if (!index[c.fips]) index[c.fips] = [];
        index[c.fips].push({
          geoid: geoid,
          name: p.name,
          primary_county: p.primary_county,
          all_counties: p.all_counties,
        });
      });
    });
    // De-duplicate place entries within each county bucket
    Object.keys(index).forEach(function (cf) {
      var seen = {};
      index[cf] = index[cf].filter(function (e) {
        if (seen[e.geoid]) return false;
        seen[e.geoid] = true;
        return true;
      });
    });
    return index;
  }

  /** Lookup by 7-digit place geoid. Returns the place record or null. */
  function lookup(geoid) {
    if (!_cache) return null;
    var places = _cache.places || {};
    return places[String(geoid).padStart(7, '0')] || null;
  }

  /** Lookup all cross-county places that touch a given county. */
  function lookupByCountyFips(countyFips) {
    if (!_byCounty) return [];
    return _byCounty[String(countyFips).padStart(5, '0')] || [];
  }

  /** Build banner HTML for a place record. Returns '' for non-cross-county. */
  function formatBanner(placeRecord) {
    if (!placeRecord || !Array.isArray(placeRecord.all_counties) || placeRecord.all_counties.length < 2) {
      return '';
    }
    var primary = placeRecord.all_counties[0];
    var others = placeRecord.all_counties.slice(1);
    var others_text = others.map(function (c) {
      return c.name + ' (~' + Number(c.population).toLocaleString() + ')';
    }).join(', ');
    return (
      '<strong>⚠ Cross-county jurisdiction.</strong> ' +
      placeRecord.name + ' spans ' + placeRecord.all_counties.length +
      ' counties. Primary: <strong>' + primary.name + '</strong> ' +
      '(~' + Number(primary.population).toLocaleString() + ' pop). ' +
      'Also touches: ' + others_text + '. ' +
      'HUD AMI is set per county — verify your site\'s actual county before relying on tier rents.'
    );
  }

  /** Build a county-level summary banner: lists cross-county places in this
   *  county. Returns '' if none. Useful when only the county is known
   *  (e.g. Deal Calculator before a site is geocoded). */
  function formatCountyBanner(countyFips) {
    var places = lookupByCountyFips(countyFips);
    if (!places.length) return '';
    var examples = places.slice(0, 5).map(function (p) { return p.name; }).join(', ');
    var more = places.length > 5 ? ' (and ' + (places.length - 5) + ' more)' : '';
    return (
      'ℹ HUD AMI is set at the county level. The following towns/CDPs in this region span ' +
      'multiple counties: <strong>' + examples + '</strong>' + more + '. ' +
      'If your site is in one of these, verify the parcel\'s actual county.'
    );
  }

  // Expose API
  window.CrossCountyDisclosure = {
    init: init,
    lookup: lookup,
    lookupByCountyFips: lookupByCountyFips,
    formatBanner: formatBanner,
    formatCountyBanner: formatCountyBanner,
  };
})();
