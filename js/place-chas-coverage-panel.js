/**
 * place-chas-coverage-panel.js
 *
 * Renders the "Place-Level Data Coverage" panel on
 * dashboard-data-quality.html. Surfaces transparency metrics for the
 * TIGER spatial-join place-CHAS pipeline so analysts can see at a
 * glance which places get tract-aggregated CHAS vs which fall back to
 * county-level data.
 *
 * Why this matters
 * ----------------
 * The dashboard mixes two CHAS methodologies depending on the place:
 *   1. Tract-aggregated (TIGER 2024 spatial join) — accurate even for
 *      cross-county jurisdictions like Erie, Aurora, Longmont
 *   2. Primary-county fallback — used when the place isn't covered by
 *      the TIGER pipeline (~4% of places as of 2026-05-10)
 *
 * Without this panel, a user can't tell which methodology a given
 * place uses. The panel shows the buckets explicitly so the
 * methodological provenance is auditable.
 *
 * Data source: data/hna/place-chas-coverage-stats.json
 *   (regenerated by scripts/hna/build_place_chas_coverage_stats.py
 *    every time place-chas.json or the alias map updates)
 */
(function () {
  'use strict';

  // Path is RELATIVE to data/. DataService.baseData() prepends 'data/'
  // (see PR #791 fix); the standalone fallback below also prepends 'data/'.
  var STATS_PATH = 'hna/place-chas-coverage-stats.json';

  function _resolveDataUrl(rel) {
    if (typeof window !== 'undefined' && window.DataService
        && typeof window.DataService.baseData === 'function') {
      return window.DataService.baseData(rel);
    }
    return 'data/' + rel;
  }

  function _fetchJson(url) {
    if (typeof window !== 'undefined' && window.DataService && window.DataService.getJSON) {
      return window.DataService.getJSON(url);
    }
    return fetch(url).then(function (r) { return r.json(); });
  }

  function init() {
    var mount = document.getElementById('placeChasCoverage');
    if (!mount) return;
    var statusEl = document.getElementById('placeChasCoverageStatus');
    if (statusEl) statusEl.textContent = 'Loading…';

    _fetchJson(_resolveDataUrl(STATS_PATH))
      .then(function (doc) {
        render(mount, doc);
        if (statusEl) {
          var ts = doc && doc.meta && doc.meta.generated_at;
          statusEl.textContent = 'Generated: ' + (ts || 'unknown');
        }
      })
      .catch(function (err) {
        mount.innerHTML = '<div class="dq-error">Coverage stats unavailable: ' +
          (err && err.message ? err.message : err) + '</div>';
      });
  }

  function render(mount, doc) {
    if (!doc || !doc.totals) return;
    var t = doc.totals;
    var pct = t.coverage_pct;
    var directPct = ((t.covered_direct / t.registry_places) * 100).toFixed(1);
    var aliasPct  = ((t.covered_via_alias / t.registry_places) * 100).toFixed(1);
    var zeroPct   = ((t.covered_with_zero_apportion / t.registry_places) * 100).toFixed(1);
    var fallbackPct = ((t.uncovered_county_fallback / t.registry_places) * 100).toFixed(1);

    var html = '';
    html += '<div class="pc-cov-grid" style="display:grid;grid-template-columns:repeat(auto-fit,minmax(180px,1fr));gap:.6rem;margin-bottom:1rem;">';
    html += _statCard('✓ Direct TIGER place-CHAS', t.covered_direct, directPct + '%',
      'rgba(34,197,94,0.10)', 'rgba(34,197,94,0.5)',
      'Tract-aggregated; methodologically consistent with the cross-county arc.');
    html += _statCard('✓ Phantom-alias resolved', t.covered_via_alias, aliasPct + '%',
      'rgba(34,197,94,0.10)', 'rgba(34,197,94,0.5)',
      'Registry duplicates (Pueblo, Englewood, Parker, etc.) resolved to canonical TIGER GEOIDs via PR #791.');
    html += _statCard('⚠ Zero apportionment', t.covered_with_zero_apportion, zeroPct + '%',
      'rgba(234,179,8,0.10)', 'rgba(234,179,8,0.5)',
      'Tiny rural places (Eads, Walden, Dinosaur) where area-weighted apportionment of a tract sliver rounds to zero. ' +
      'Currently shows as low-confidence; future PR may use rate-only fallback.');
    html += _statCard('✗ County fallback', t.uncovered_county_fallback, fallbackPct + '%',
      'rgba(239,68,68,0.10)', 'rgba(239,68,68,0.5)',
      'No TIGER coverage; place-level CHAS chart shows the containing county\'s rates instead. ' +
      'Mostly military reservations (Fort Carson), tribal lands (Southern Ute), and U2-class CDPs.');
    html += '</div>';

    // Top-line coverage summary
    html += '<p style="font-size:.95rem;margin:0 0 1rem">' +
      '<strong>' + pct + '%</strong> of <strong>' + t.registry_places + '</strong> registry places ' +
      'have tract-aggregated place-CHAS via TIGER 2024 spatial join. ' +
      '<strong>' + t.uncovered_county_fallback + '</strong> places fall back to county-level data.' +
      '</p>';

    // Uncovered places list
    var uncovered = doc.uncovered_places || [];
    if (uncovered.length) {
      html += '<details style="margin-top:.5rem;">';
      html += '<summary style="cursor:pointer;font-weight:600;font-size:.92rem;">';
      html += 'Uncovered places (' + uncovered.length + ' of ' + t.registry_places + ')';
      html += '</summary>';
      html += '<ul style="margin-top:.5rem;font-size:.84rem;line-height:1.5;list-style:disc;padding-left:1.25rem;">';
      uncovered.slice(0, 50).forEach(function (p) {
        var countyName = (doc.by_county[p.county_fips] || {}).name || p.county_fips;
        html += '<li>' + _esc(p.name) + ' <span style="color:var(--muted)">(' + _esc(countyName) + ' County)</span></li>';
      });
      if (uncovered.length > 50) {
        html += '<li style="color:var(--muted);font-style:italic">... and ' + (uncovered.length - 50) + ' more</li>';
      }
      html += '</ul></details>';
    }

    // Per-county breakdown for counties with any gaps
    var byCounty = doc.by_county || {};
    var countiesWithGaps = Object.keys(byCounty)
      .filter(function (k) { return byCounty[k].uncovered > 0; })
      .sort(function (a, b) { return byCounty[b].uncovered - byCounty[a].uncovered; });
    if (countiesWithGaps.length) {
      html += '<details style="margin-top:.75rem;">';
      html += '<summary style="cursor:pointer;font-weight:600;font-size:.92rem;">';
      html += 'Per-county breakdown (' + countiesWithGaps.length + ' counties with gaps)';
      html += '</summary>';
      html += '<table style="width:100%;border-collapse:collapse;margin-top:.5rem;font-size:.85rem;">';
      html += '<thead><tr>' +
        '<th style="text-align:left;padding:.3rem .5rem;border-bottom:1px solid var(--border);">County</th>' +
        '<th style="text-align:right;padding:.3rem .5rem;border-bottom:1px solid var(--border);">Total</th>' +
        '<th style="text-align:right;padding:.3rem .5rem;border-bottom:1px solid var(--border);">Covered</th>' +
        '<th style="text-align:right;padding:.3rem .5rem;border-bottom:1px solid var(--border);">Uncovered</th>' +
        '<th style="text-align:right;padding:.3rem .5rem;border-bottom:1px solid var(--border);">%</th>' +
        '</tr></thead><tbody>';
      countiesWithGaps.forEach(function (fips) {
        var c = byCounty[fips];
        html += '<tr>' +
          '<td style="padding:.3rem .5rem;border-bottom:1px solid var(--border-soft,#222);">' + _esc(c.name) + '</td>' +
          '<td style="text-align:right;padding:.3rem .5rem;border-bottom:1px solid var(--border-soft,#222);font-variant-numeric:tabular-nums;">' + c.total_places + '</td>' +
          '<td style="text-align:right;padding:.3rem .5rem;border-bottom:1px solid var(--border-soft,#222);font-variant-numeric:tabular-nums;">' + c.covered + '</td>' +
          '<td style="text-align:right;padding:.3rem .5rem;border-bottom:1px solid var(--border-soft,#222);font-variant-numeric:tabular-nums;color:var(--bad,#dc2626);">' + c.uncovered + '</td>' +
          '<td style="text-align:right;padding:.3rem .5rem;border-bottom:1px solid var(--border-soft,#222);font-variant-numeric:tabular-nums;">' + c.coverage_pct + '%</td>' +
        '</tr>';
      });
      html += '</tbody></table></details>';
    }

    mount.innerHTML = html;
  }

  function _statCard(label, count, pct, bg, border, tooltip) {
    return (
      '<div class="dq-card pc-cov-card" title="' + _esc(tooltip) + '" ' +
      'style="background:' + bg + ';border:1px solid ' + border + ';' +
      'padding:.7rem;border-radius:6px;">' +
      '<div style="font-size:.78rem;color:var(--muted);margin-bottom:.3rem;">' + _esc(label) + '</div>' +
      '<div style="font-size:1.35rem;font-weight:700;font-variant-numeric:tabular-nums;">' + count + '</div>' +
      '<div style="font-size:.85rem;color:var(--muted);font-variant-numeric:tabular-nums;">' + pct + '</div>' +
      '</div>'
    );
  }

  function _esc(s) {
    return String(s == null ? '' : s)
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&#39;');
  }

  // Boot when DOM ready
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

  window.PlaceChasCoveragePanel = { init: init, render: render };
})();
