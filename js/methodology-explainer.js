/**
 * methodology-explainer.js
 *
 * Drop-in component that adds a hoverable "ℹ How is this computed?"
 * icon next to chart titles, with plain-language explanations of the
 * methodology behind each chart/score.
 *
 * Adapted from striblab's editorial pattern (comparison-review C3):
 * make methodology auditable and visible at the point of consumption,
 * not buried in a docs page.
 *
 * Usage
 * -----
 * Auto-attaches to any element with [data-methodology-key="<key>"]
 * on page load. Looks up the key in METHODOLOGY_REGISTRY and renders
 * a `<details>` summary/body next to the title.
 *
 * Example HTML:
 *   <h2>Renter cost burden by AMI <span data-methodology-key="chas-cb"></span></h2>
 *
 * Renders to:
 *   <h2>Renter cost burden by AMI
 *     <span class="me-icon" tabindex="0" title="How is this computed?">ℹ</span>
 *     <div class="me-pop" hidden>...</div>
 *   </h2>
 */
(function () {
  'use strict';

  var METHODOLOGY_REGISTRY = {
    'chas-cb': {
      title: 'Renter cost burden by AMI',
      what: 'Households paying ≥30% of income on housing costs, broken out by AMI tier (≤30%, 31-50%, 51-80%, 81-100%, >100% of HUD Area Median Family Income).',
      how: 'HUD CHAS Table 7 (2018-2022 vintage). Counts are summed across the 5 household types within each AMI tier. Severe burden (>50%) is a subset of moderate-or-severe (>30%).',
      caveats: 'CHAS publishes at county granularity. Place-level rates come from TIGER 2024 area-weighted apportionment of overlapping tracts (96.1% of registry places covered).',
      source: 'data/market/chas_co.json (county) + data/hna/place-chas.json (place)',
    },
    'tiger-place-chas': {
      title: 'TIGER 2024 place-level CHAS',
      what: 'CHAS metrics aggregated up to municipal place boundaries (Aurora, Erie, etc.) using Census TIGER 2024 spatial join.',
      how: 'For each place, sum each overlapping tract\'s CHAS counts weighted by share_of_tract_area (the fraction of the tract that falls inside the place).',
      caveats: 'Area-weighted apportionment assumes uniform population density within a tract. Block-level relationship files would be more accurate but at 100× data volume.',
      source: 'data/hna/place-chas.json (regenerated by scripts/hna/build_place_chas.py)',
    },
    'cross-county-disclosure': {
      title: 'Cross-county jurisdiction disclosure',
      what: '26 Colorado towns/CDPs physically span multiple counties (Aurora, Erie, Longmont, etc.). HUD AMI is per-county, so a parcel on the wrong side of the line falls into a different AMI tier.',
      how: 'Built by querying Census ACS county-(or-part) hierarchy for every CO place. Multi-county places are flagged with their primary county (highest pop) + all touched counties.',
      caveats: 'Primary county is defined by population, not area. The "right" county for HUD AMI is the one where the parcel sits, which the parcel→county lookup tool resolves.',
      source: 'data/hna/cross-county-places.json',
    },
    'pma-composite': {
      title: 'PMA composite score',
      what: 'Composite score (0-100) blending 9 dimensions: rent pressure, transit, employment access, school quality, etc. Designed to match NH&RA / Novogradac PMA standards.',
      how: 'Each dimension is normalized to 0-100, then weighted-averaged. Weights are documented in the PMA scoring engine source.',
      caveats: 'It\'s a market-positioning benchmark, not a CHFA QAP score. CHFA QAP scoring is a separate model.',
      source: 'js/market-analysis.js + js/pma-confidence.js',
    },
    'hmda-credit-access': {
      title: 'HMDA mortgage credit access',
      what: 'County-level mortgage origination volume, denial rates, mean loan amount, and multifamily-only originations from the federal Home Mortgage Disclosure Act database.',
      how: 'Pulled from the CFPB HMDA Data Browser API. Monthly refresh; vintages 2018-current.',
      caveats: 'Multifamily category covers HMDA dwelling_categories="Multifamily:Site-Built". Does not include 2-4 unit properties (those count as single-family in HMDA).',
      source: 'data/hmda/co-county-aggregates.json + data/hmda/co-state-trends.json',
    },
    'prop123-baseline': {
      title: 'Prop 123 baseline + growth target',
      what: 'Proposition 123 requires participating CO jurisdictions to commit to 3% annual growth in affordable housing units.',
      how: 'Baseline currently estimated as 6% of total housing units (heuristic). Future PR will pull real baselines from CDOLA jurisdictional filings.',
      caveats: 'The 3% target is a CDOLA commitment, not federal LIHTC. Actual compliance reporting is pending CDOLA filing aggregation.',
      source: 'data/policy/prop123_jurisdictions.json',
    },
    'tier-shares': {
      title: 'AMI tier-share apportionment',
      what: 'When projecting household demand by AMI tier (HousingDemand chart), each year\'s HH growth is split into 5 tiers using real CHAS-derived renter shares for the selected geography.',
      how: 'Priority: TIGER place-CHAS → county CHAS Table 7 → CO statewide baseline. Source label appears in chart tooltip footer.',
      caveats: 'Assumes the tier mix stays constant over the projection horizon. Real mix shifts with income growth + migration.',
      source: 'js/chas-tier-shares.js',
    },
    'rent-vs-buy': {
      title: 'Rent vs Buy breakeven',
      what: 'Year at which cumulative ownership cost (mortgage + taxes + maintenance - principal paydown - appreciation) drops below cumulative rent paid.',
      how: 'Standard 30-year mortgage amortization with editable inputs. Rent escalates at the user-specified rate; home value appreciates at the user-specified rate.',
      caveats: 'Does not model tax deductions, capital gains exclusion, or moving costs. Intended for directional analysis, not personal financial planning.',
      source: 'js/rent-vs-buy-breakeven.js',
    },
    'affordability-ratios': {
      title: 'Affordability ratios (P/I, P/R, Affordability rate)',
      what: 'Three industry-standard market-positioning ratios per county: price-to-income, price-to-rent, and a derived affordability rate.',
      how: 'Price/Income = median home value / median HHI. Price/Rent = home value / (rent × 12). Affordability rate = estimate of CO HHs that can afford the median home at the current MORTGAGE30US rate, 30% PITI rule.',
      caveats: 'Affordability rate uses a median-income proxy (true rate needs per-county HH income distribution). CO statewide median rent is used for P/R until per-county rent data is wired.',
      source: 'data/co-county-economic-indicators.json + FRED MORTGAGE30US',
    },
    'market-health': {
      title: 'Market Health Composite',
      what: 'Per-county 0-100 score blending 5 signals: 5-year job growth, 5-year population growth, inverse unemployment, HMDA origination volume, and inverse HMDA denial rate. Higher = stronger market.',
      how: 'Each metric is percentile-ranked across the 64 CO counties (inverted for unemployment and denial). The 5 percentile scores are equal-weighted and averaged. No demographic / political adjustments.',
      caveats: 'Equal-weighting is a deliberate simplification. Investor-specific weightings (e.g. emphasize denial rate for LIHTC bond markets) would shift rankings. Single-vintage snapshot; trend not blended in.',
      source: 'data/co-county-economic-indicators.json + data/hmda/co-county-aggregates.json',
    },
  };

  function _esc(s) {
    return String(s == null ? '' : s)
      .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;').replace(/'/g, '&#39;');
  }

  function _buildPopover(entry) {
    return '<div class="me-pop-body" style="font-size:.82rem;line-height:1.5;">' +
      '<p style="margin:0 0 .4rem;"><strong>What:</strong> ' + _esc(entry.what) + '</p>' +
      '<p style="margin:0 0 .4rem;"><strong>How:</strong> ' + _esc(entry.how) + '</p>' +
      (entry.caveats
        ? '<p style="margin:0 0 .4rem;"><strong>Caveats:</strong> ' + _esc(entry.caveats) + '</p>'
        : '') +
      (entry.source
        ? '<p style="margin:0;font-size:.74rem;color:var(--muted);"><strong>Source:</strong> <code>' + _esc(entry.source) + '</code></p>'
        : '') +
    '</div>';
  }

  function attach() {
    var anchors = document.querySelectorAll('[data-methodology-key]');
    anchors.forEach(function (anchor) {
      if (anchor.dataset.meAttached) return;
      anchor.dataset.meAttached = '1';
      var key = anchor.dataset.methodologyKey;
      var entry = METHODOLOGY_REGISTRY[key];
      if (!entry) return;
      // Render icon + popover
      var icon = document.createElement('span');
      icon.className = 'me-icon';
      icon.setAttribute('role', 'button');
      icon.setAttribute('tabindex', '0');
      icon.setAttribute('aria-label', 'How is "' + entry.title + '" computed?');
      icon.title = 'Methodology: ' + entry.title;
      icon.style.cssText =
        'display:inline-flex;align-items:center;justify-content:center;' +
        'width:18px;height:18px;margin-left:.3rem;padding:0;' +
        'border:1px solid var(--accent,#096e65);border-radius:50%;' +
        'background:rgba(9,110,101,.08);color:var(--accent,#096e65);' +
        'font-size:.72rem;font-weight:700;cursor:help;vertical-align:middle;';
      icon.textContent = 'ℹ';

      var pop = document.createElement('div');
      pop.className = 'me-pop';
      pop.hidden = true;
      pop.style.cssText =
        'position:absolute;z-index:1000;max-width:380px;padding:.75rem 1rem;' +
        'background:var(--bg,#fff);border:1px solid var(--border);border-radius:6px;' +
        'box-shadow:0 4px 16px rgba(0,0,0,.15);margin-top:.25rem;line-height:1.4;';
      pop.innerHTML = '<div style="font-weight:600;font-size:.92rem;margin-bottom:.4rem;">' +
        _esc(entry.title) + '</div>' + _buildPopover(entry);

      anchor.appendChild(icon);
      anchor.appendChild(pop);
      anchor.style.position = 'relative';

      function show() { pop.hidden = false; }
      function hide() { pop.hidden = true; }
      function toggle() { pop.hidden = !pop.hidden; }
      icon.addEventListener('click', toggle);
      icon.addEventListener('keydown', function (e) {
        if (e.key === 'Enter' || e.key === ' ') { e.preventDefault(); toggle(); }
        else if (e.key === 'Escape') { hide(); }
      });
      icon.addEventListener('mouseenter', show);
      anchor.addEventListener('mouseleave', hide);
    });
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', attach);
  } else {
    attach();
  }

  // Re-scan periodically for dynamically-added anchors (e.g. after
  // tab switches, async renders)
  var _observer;
  if (typeof MutationObserver !== 'undefined') {
    _observer = new MutationObserver(function () { attach(); });
    document.addEventListener('DOMContentLoaded', function () {
      _observer.observe(document.body, { childList: true, subtree: true });
    });
  }

  window.MethodologyExplainer = {
    attach: attach,
    REGISTRY: METHODOLOGY_REGISTRY,
  };
})();
