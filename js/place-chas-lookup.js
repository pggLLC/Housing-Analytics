/**
 * place-chas-lookup.js
 *
 * Browser-side helper for the TIGER-derived place-level CHAS data
 * produced by the TIGER spatial-join arc:
 *   PR-C1 — tract-level CHAS aggregations
 *   PR-C2 — TIGER place→tract spatial membership
 *   PR-C3 (this) — place-level CHAS via area-weighted apportionment
 *
 * The dashboard previously inherited the place's "primary county"
 * CHAS rates, which was wrong for the 26 cross-county jurisdictions
 * (Aurora, Erie, Longmont, etc.). With this module, place-level CHAS
 * comes from summing the underlying tracts' CHAS counts weighted by
 * what fraction of each tract sits inside the place — accurate for
 * any place regardless of how many counties it spans.
 *
 * Public API
 * ----------
 *   window.PlaceChas.init() — fetch + cache the data file
 *   window.PlaceChas.lookup(geoid) — returns place CHAS or null
 *   window.PlaceChas.compareToCounty(placeGeoid, countyChasRecord)
 *     — returns {place, county, delta} for cost-burden rates so a
 *       caller can show "Erie's renter cost-burden rate is X% vs Weld
 *       County's Y%". Useful for surfacing the cross-county-line
 *       impact in plain language.
 *
 * Data source
 * -----------
 *   data/hna/place-chas.json (regenerated by
 *   scripts/hna/build_place_chas.py).
 */
(function () {
  'use strict';

  var DATA_URL = 'data/hna/place-chas.json';
  var _cache = null;
  var _loadPromise = null;

  function _resolveDataUrl() {
    if (typeof window !== 'undefined' && window.DataService
        && typeof window.DataService.baseData === 'function') {
      return window.DataService.baseData(DATA_URL);
    }
    return DATA_URL;
  }

  function init() {
    if (_cache) return Promise.resolve(_cache);
    if (_loadPromise) return _loadPromise;
    var url = _resolveDataUrl();
    var loader = (typeof window !== 'undefined' && window.DataService && window.DataService.getJSON)
      ? window.DataService.getJSON(url)
      : fetch(url).then(function (r) { return r.json(); });
    _loadPromise = loader.then(function (doc) {
      _cache = doc || { places: {}, meta: {} };
      return _cache;
    }).catch(function (err) {
      console.warn('[place-chas-lookup] Could not load ' + url + ':', err);
      _cache = { places: {}, meta: {} };
      return _cache;
    });
    return _loadPromise;
  }

  /** Lookup place CHAS by 7-digit place geoid. Returns null if not present. */
  function lookup(geoid) {
    if (!_cache) return null;
    return (_cache.places || {})[String(geoid).padStart(7, '0')] || null;
  }

  /** Given a place geoid + a county CHAS record (from data/market/chas_co.json),
   *  return a comparison object useful for surfacing the cross-county-line
   *  impact:
   *    {
   *      place:  { renter_cb30, renter_cb50, owner_cb30, owner_cb50 },
   *      county: { renter_cb30, renter_cb50, owner_cb30, owner_cb50 },
   *      delta:  { renter_cb30_pp, renter_cb50_pp, owner_cb30_pp, owner_cb50_pp }
   *    }
   *  pp = percentage-point difference (place rate − county rate).
   */
  function compareToCounty(placeGeoid, countyChasRecord) {
    var place = lookup(placeGeoid);
    if (!place || !countyChasRecord || !countyChasRecord.summary) return null;
    var ps = place.summary;
    var cs = countyChasRecord.summary;
    var countyRenterTotal = cs.total_renter_hh || 0;
    var countyOwnerTotal  = cs.total_owner_hh  || 0;
    var county = {
      renter_cb30: countyRenterTotal ? cs.renter_cb30_count / countyRenterTotal : 0,
      renter_cb50: countyRenterTotal ? cs.renter_cb50_count / countyRenterTotal : 0,
      owner_cb30:  countyOwnerTotal  ? cs.owner_cb30_count  / countyOwnerTotal  : 0,
      owner_cb50:  countyOwnerTotal  ? cs.owner_cb50_count  / countyOwnerTotal  : 0,
    };
    var placeRates = {
      renter_cb30: ps.renter_cb30_share,
      renter_cb50: ps.renter_cb50_share,
      owner_cb30:  ps.owner_cb30_share,
      owner_cb50:  ps.owner_cb50_share,
    };
    return {
      place: placeRates,
      county: county,
      delta: {
        renter_cb30_pp: (placeRates.renter_cb30 - county.renter_cb30) * 100,
        renter_cb50_pp: (placeRates.renter_cb50 - county.renter_cb50) * 100,
        owner_cb30_pp:  (placeRates.owner_cb30  - county.owner_cb30)  * 100,
        owner_cb50_pp:  (placeRates.owner_cb50  - county.owner_cb50)  * 100,
      },
    };
  }

  /** Format a comparison result as a short HTML banner. */
  function formatComparison(comparison, placeName, countyName) {
    if (!comparison) return '';
    var d = comparison.delta;
    var direction = d.renter_cb30_pp > 0 ? 'higher' : 'lower';
    var sign = d.renter_cb30_pp > 0 ? '+' : '−';
    return (
      '<strong>' + (placeName || 'This place') + '</strong> renter cost-burden rate is ' +
      (comparison.place.renter_cb30 * 100).toFixed(1) + '% — ' +
      Math.abs(d.renter_cb30_pp).toFixed(1) + 'pp ' + direction + ' than ' +
      (countyName || 'the surrounding county') + ' (' +
      (comparison.county.renter_cb30 * 100).toFixed(1) + '%). ' +
      'Severe burden (>50% of income): ' +
      (comparison.place.renter_cb50 * 100).toFixed(1) + '% vs ' +
      (comparison.county.renter_cb50 * 100).toFixed(1) + '%.'
    );
  }

  window.PlaceChas = {
    init: init,
    lookup: lookup,
    compareToCounty: compareToCounty,
    formatComparison: formatComparison,
  };
})();
