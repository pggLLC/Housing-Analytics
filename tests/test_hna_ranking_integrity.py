class TestEnrichmentCompleteness:
    """Every well-known city should have a non-empty metrics dict.
    Boulder (city) was once stripped of all enrichment due to a name-key
    mismatch — make sure that doesn't recur."""

    REQUIRED_METRIC_KEYS = (
        'housing_gap_units',
        'pct_cost_burdened',
        'in_commuters',
        'population',
        'population_projection_20yr',
        'pct_renters',
    )

    @pytest.mark.parametrize('place_name', ['Boulder (city)', 'Denver (city)', 'Aurora (city)'])
    def test_well_known_city_has_full_enrichment(self, entries, place_name):
        entry = next((e for e in entries if e.get('name') == place_name), None)
        assert entry is not None, f'{place_name} missing from ranking-index'
        m = entry['metrics']
        missing = [k for k in self.REQUIRED_METRIC_KEYS if k not in m]
        assert not missing, (
            f'{place_name} missing metric keys: {missing}'
        )
        assert m.get('population', 0) > 0, (
            f'{place_name} has population=0 — likely orphaned entry'
        )


