class TestNamingFormat:
    """Place/CDP names must use the parenthetical form so name-based joins
    against geo-config.json and geography-registry.json keep working."""

    def test_every_place_name_uses_parens(self, entries):
        bad = [
            f"{e['geoid']}: {e['name']!r}"
            for e in entries
            if e.get('type') == 'place' and '(' not in e.get('name', '')
        ]
        assert not bad, (
            'Place entries missing parenthetical suffix in name '
            '(e.g. "Boulder city" instead of "Boulder (city)"). '
            'Breaks name-based joins to geo-config.json.\n'
            + '\n'.join(bad[:10])
        )

    def test_every_cdp_name_uses_parens(self, entries):
        bad = [
            f"{e['geoid']}: {e['name']!r}"
            for e in entries
            if e.get('type') == 'cdp' and '(' not in e.get('name', '')
        ]
        assert not bad, (
            'CDP entries missing parenthetical suffix in name. '
            'Breaks name-based joins.\n'
            + '\n'.join(bad[:10])
        )

    def test_county_names_end_with_county_suffix(self, entries):
        bad = [
            f"{e['geoid']}: {e['name']!r}"
            for e in entries
            if e.get('type') == 'county' and not e.get('name', '').endswith('County')
        ]
        # Broomfield is "Broomfield County" too, no exceptions expected
        assert not bad, (
            'County entries should be labeled "<Name> County".\n'
            + '\n'.join(bad[:5])
        )


