"""tests/test_hna_ranking_integrity.py
Regression guards for `data/hna/ranking-index.json` produced by
`scripts/hna/build_ranking_index.py`.

Prior bugs these probes exist to prevent reintroducing:

1. Naming inconsistency between ranking-index and geo-config —
   ranking-index used to ship "Boulder city" while geo-config used
   "Boulder (city)", causing name-based joins (dropdowns, comparison
   selectors) to silently fail.

2. County-level LEHD inflow leaked into place/CDP rows. Fruita (city)
   used to show Mesa County's full ~12,400 in-commuter count.

3. Owner % stored as renter % — Fruita used to ship pct_renters=79.9
   (the owner percentage). Symptom of stale data generated by an older
   buggy script and never re-built.

4. Boulder (city) entry stripped of all enrichment metrics due to
   keying mismatch downstream of #1.

These tests assert structural invariants on the *current* on-disk
ranking-index. Run after every regeneration. They are cheap (one JSON
load).
"""

import glob
import importlib.util
import json
import math
import os

import pytest

REPO_ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), '..'))
RANKING_PATH = os.path.join(REPO_ROOT, 'data', 'hna', 'ranking-index.json')
SUMMARY_DIR  = os.path.join(REPO_ROOT, 'data', 'hna', 'summary')


@pytest.fixture(scope='module')
def ranking():
    """Load ranking-index.json once per test module."""
    with open(RANKING_PATH, 'r', encoding='utf-8') as f:
        return json.load(f)


@pytest.fixture(scope='module')
def entries(ranking):
    """All ranking entries as a list."""
    return ranking['rankings']


@pytest.fixture(scope='module')
def counties_by_fips(entries):
    """County entries keyed by 5-digit FIPS for cross-joins."""
    return {e['geoid']: e for e in entries if e.get('type') == 'county'}


@pytest.fixture(scope='module')
def ranking_builder():
    """Load build_ranking_index.py as a module for helper-level guards."""
    path = os.path.join(REPO_ROOT, 'scripts', 'hna', 'build_ranking_index.py')
    spec = importlib.util.spec_from_file_location('build_ranking_index', path)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


