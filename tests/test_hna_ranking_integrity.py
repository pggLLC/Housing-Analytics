# LEHD inflow scaling (Bug #2)
# ---------------------------------------------------------------------------

class TestLehdInflowScaling:
    """A place/CDP's in_commuters must never exceed its enclosing county's
    in_commuters. If it does, the LEHD county-total leaked into the place
    row instead of being scaled by population share."""

    def _containing_county_fips(self, geoid):
        """Read containingCounty from the place/CDP's summary file."""
        path = os.path.join(SUMMARY_DIR, f'{geoid}.json')
        if not os.path.exists(path):
            return None
        with open(path, 'r', encoding='utf-8') as f:
            return json.load(f).get('geo', {}).get('containingCounty')

    def test_place_in_commuters_does_not_exceed_county(self, entries, counties_by_fips):
        """Guard against the old county-total leak.

        Places spanning multiple counties can legitimately exceed any
        single containing county's inflow once place-LEHD apportionment
        is in play (per the spatial-membership method documented in
        data/hna/place-lehd.json:meta.method). Skip rows that resolved
        via the place-LEHD path — the assertion only meaningfully
        guards the legacy ``county_proportional`` fallback.
        """
        violations = []
        for e in entries:
            if e.get('type') not in ('place', 'cdp'):
                continue
            # Place-LEHD apportions across the tracts a place spans —
            # for multi-county places the sum can exceed any single
            # county. The leak this test was built to catch only
            # affected the legacy county-scaled fallback.
            if e['metrics'].get('_lehd_source') == 'place':
                continue
            place_inflow = e['metrics'].get('in_commuters', 0)
            if place_inflow == 0:
                continue
            county_fips = self._containing_county_fips(e['geoid'])
            if not county_fips or county_fips not in counties_by_fips:
                continue
            county_inflow = counties_by_fips[county_fips]['metrics'].get('in_commuters', 0)
            if county_inflow == 0:
                continue
            if place_inflow > county_inflow:
                violations.append(
                    f"{e['name']} ({e['geoid']}): place={place_inflow:,} > "
                    f"county {county_fips}={county_inflow:,}"
                )
        assert not violations, (
            'LEHD county-total leaked into place/CDP rows '
            '(in_commuters should be scaled by population share).\n'
            + '\n'.join(violations[:10])
        )

    def test_fruita_in_commuters_within_plausible_range(self, entries):
        """Fruita (city) had ~12,400 in_commuters before the fix (the full
        Mesa County total). Scaled by population share, it should be a
        small fraction of that — under 3,000."""
        fruita = next(
            (e for e in entries if e.get('name') == 'Fruita (city)'),
            None,
        )
        assert fruita is not None, 'Fruita (city) missing from ranking-index'
        inflow = fruita['metrics'].get('in_commuters', 0)
        assert 0 < inflow < 3000, (
            f'Fruita (city) in_commuters={inflow:,} is implausible. '
            'Expected a small fraction of Mesa County\'s ~12k. '
            'Likely regression of LEHD county-total leak.'
        )


