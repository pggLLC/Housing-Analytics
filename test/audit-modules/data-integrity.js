'use strict';

/**
 * Data Integrity & Completeness Audit Module
 *
 * Scans jurisdiction-level datasets, data source endpoints, and critical JSON
 * files for missing, null, stale, or out-of-range values.
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..', '..');
const DATA_DIR = path.join(ROOT, 'data');

// Colorado has exactly 64 counties (Rule 4)
const EXPECTED_CO_COUNTY_COUNT = 64;

// Maximum age (days) before a data file is considered stale
const STALE_DATA_THRESHOLD_DAYS = 30;

// Required sentinel keys per file (Rule 18)
const SENTINEL_KEYS = {
    'fred-data.json':             ['updated'],
    'chfa-lihtc.json':            ['fetchedAt'],
    'co_ami_gap_by_county.json':  ['meta'],
    'manifest.json':              ['generated'],
};

// Required numeric fields that must not be null (Rule 2)
const REQUIRED_NUMERIC_FIELDS = ['ami_4person', 'LI_UNITS', 'N_UNITS', 'CREDIT', 'NON_PROF', 'DDA'];

/**
 * Checks whether a FIPS code is a valid 5-digit Colorado county FIPS (Rule 1).
 * @param {string} fips
 * @returns {boolean}
 */
function isValidFips(fips) {
    return typeof fips === 'string' && /^\d{5}$/.test(fips);
}

/**
 * Returns the age of a file in days, or Infinity if stat fails.
 * @param {string} filePath
 * @returns {number}
 */
function fileAgeDays(filePath) {
    try {
        const stat = fs.statSync(filePath);
        return (Date.now() - stat.mtimeMs) / (1000 * 60 * 60 * 24);
    } catch (_) {
        return Infinity;
    }
}

/**
 * Safely parses a JSON file. Returns null on error.
 * @param {string} filePath
 * @returns {any|null}
 */
function safeReadJson(filePath) {
    try {
        return JSON.parse(fs.readFileSync(filePath, 'utf8'));
    } catch (_) {
        return null;
    }
}

/**
 * Checks sentinel metadata keys required in critical data files (Rule 18).
 * @returns {Array<object>} issues
 */
function checkSentinelKeys() {
    const issues = [];
    for (const [filename, keys] of Object.entries(SENTINEL_KEYS)) {
        const candidates = [
            path.join(DATA_DIR, filename),
            path.join(DATA_DIR, 'market', filename),
            path.join(DATA_DIR, 'hna', filename),
        ];
        const filePath = candidates.find(p => fs.existsSync(p));
        if (!filePath) {
            issues.push({
                severity: 'medium',
                type: 'data',
                file: `data/**/${filename}`,
                description: `File not found: ${filename}`,
                expected: 'File exists with sentinel keys: ' + keys.join(', '),
                actual: 'File not found',
                recommendation: `Ensure ${filename} is generated by its ETL workflow.`,
            });
            continue;
        }
        const data = safeReadJson(filePath);
        if (!data) {
            issues.push({
                severity: 'high',
                type: 'data',
                file: filePath.replace(ROOT + '/', ''),
                description: `Cannot parse JSON: ${filename}`,
                expected: 'Valid JSON with sentinel keys: ' + keys.join(', '),
                actual: 'JSON parse failed',
                recommendation: `Regenerate ${filename} using its ETL script.`,
            });
            continue;
        }
        for (const key of keys) {
            if (!(key in data) || data[key] === null || data[key] === undefined) {
                issues.push({
                    severity: 'high',
                    type: 'data',
                    file: filePath.replace(ROOT + '/', ''),
                    description: `Missing sentinel key "${key}" in ${filename}`,
                    expected: `Top-level key "${key}" must be present and non-null`,
                    actual: `Key "${key}" is ${key in data ? 'null/undefined' : 'absent'}`,
                    recommendation: `Update ETL script to always write the "${key}" field.`,
                });
            }
        }
    }
    return issues;
}

/**
 * Validates FIPS codes in county-level datasets (Rule 1).
 * @returns {Array<object>} issues
 */
function checkFipsCodes() {
    const issues = [];
    const targets = [
        path.join(DATA_DIR, 'co_ami_gap_by_county.json'),
        path.join(DATA_DIR, 'hna', 'lihtc-trends-by-county.json'),
    ];

    for (const filePath of targets) {
        if (!fs.existsSync(filePath)) continue;
        const data = safeReadJson(filePath);
        if (!data) continue;

        const relPath = filePath.replace(ROOT + '/', '');

        // Extract the list of FIPS values from the data structure
        let fipsValues = [];
        if (Array.isArray(data)) {
            // Array of county records — pick fips/FIPS/county_fips property
            fipsValues = data.map(d => d.fips || d.FIPS || d.county_fips).filter(Boolean);
        } else if (data.counties && Array.isArray(data.counties)) {
            // Nested under a "counties" key (e.g. co_ami_gap_by_county.json)
            fipsValues = data.counties.map(d => d.fips || d.FIPS || d.county_fips).filter(Boolean);
        } else {
            // Object keyed directly by FIPS codes
            fipsValues = Object.keys(data).filter(k => /^\d+$/.test(k));
        }

        for (const fips of fipsValues) {
            if (!isValidFips(String(fips))) {
                issues.push({
                    severity: 'critical',
                    type: 'data',
                    file: relPath,
                    description: `Invalid FIPS code: "${fips}" (must be 5 digits)`,
                    expected: '5-character zero-padded FIPS string (e.g. "08091")',
                    actual: `"${fips}"`,
                    recommendation: 'Use str(fips).zfill(5) in Python / .padStart(5,"0") in JS.',
                });
            }
        }
    }
    return issues;
}

/**
 * Checks that statewide county datasets contain all 64 counties (Rule 4).
 * @returns {Array<object>} issues
 */
function checkCountyCoverage() {
    const issues = [];
    const ltcTrends = path.join(DATA_DIR, 'hna', 'lihtc-trends-by-county.json');
    if (!fs.existsSync(ltcTrends)) {
        issues.push({
            severity: 'medium',
            type: 'data',
            file: 'data/hna/lihtc-trends-by-county.json',
            description: 'LIHTC trends by county file not found',
            expected: `File with ${EXPECTED_CO_COUNTY_COUNT} county entries`,
            actual: 'File not found',
            recommendation: 'Run the LIHTC data pipeline to regenerate this file.',
        });
        return issues;
    }
    const data = safeReadJson(ltcTrends);
    if (!data) return issues;
    const count = Array.isArray(data) ? data.length : Object.keys(data).length;
    if (count !== EXPECTED_CO_COUNTY_COUNT) {
        issues.push({
            severity: 'high',
            type: 'data',
            file: 'data/hna/lihtc-trends-by-county.json',
            description: `County coverage incomplete: found ${count} of ${EXPECTED_CO_COUNTY_COUNT} counties`,
            expected: `${EXPECTED_CO_COUNTY_COUNT} counties`,
            actual: `${count} counties`,
            recommendation: 'Re-run LIHTC data pipeline to include all 64 Colorado counties.',
        });
    }
    return issues;
}

/**
 * Checks for required numeric fields that must not be null (Rule 2).
 * @returns {Array<object>} issues
 */
function checkRequiredNumericFields() {
    const issues = [];
    const amiGap = path.join(DATA_DIR, 'co_ami_gap_by_county.json');
    if (!fs.existsSync(amiGap)) return issues;

    const data = safeReadJson(amiGap);
    if (!data) return issues;

    const records = Array.isArray(data) ? data : (data.counties || data.data || []);
    let nullCount = 0;
    for (const record of records) {
        if (record && record.ami_4person === null) nullCount++;
    }
    if (nullCount > 0) {
        issues.push({
            severity: 'high',
            type: 'data',
            file: 'data/co_ami_gap_by_county.json',
            description: `${nullCount} records have null "ami_4person" field`,
            expected: 'All counties must have a valid ami_4person value',
            actual: `${nullCount} null values found`,
            recommendation: 'Backfill missing AMI values using HUD income limits data.',
        });
    }
    return issues;
}

/**
 * Checks baseYear / pyramidYear values in projection and SYA files (Rule 3).
 * @returns {Array<object>} issues
 */
function checkProjectionYears() {
    const issues = [];
    const EXPECTED_VINTAGE = 2024;
    const scanDirs = [
        path.join(DATA_DIR, 'projections'),
        path.join(DATA_DIR, 'dola_sya'),
        path.join(DATA_DIR, 'hna'),
    ];

    for (const dir of scanDirs) {
        if (!fs.existsSync(dir)) continue;
        const files = fs.readdirSync(dir).filter(f => f.endsWith('.json'));
        for (const file of files) {
            const filePath = path.join(dir, file);
            const data = safeReadJson(filePath);
            if (!data || typeof data !== 'object') continue;
            for (const key of ['baseYear', 'pyramidYear']) {
                if (key in data && data[key] !== EXPECTED_VINTAGE) {
                    issues.push({
                        severity: 'high',
                        type: 'data',
                        file: filePath.replace(ROOT + '/', ''),
                        description: `Incorrect ${key}: ${data[key]} (expected ${EXPECTED_VINTAGE})`,
                        expected: `${key}: ${EXPECTED_VINTAGE}`,
                        actual: `${key}: ${data[key]}`,
                        recommendation: `Update ${key} to ${EXPECTED_VINTAGE} in the ETL script.`,
                    });
                }
            }
        }
    }
    return issues;
}

/**
 * Checks data/manifest.json for staleness and minimum file count (Rule 5).
 * @returns {Array<object>} issues
 */
function checkManifest() {
    const issues = [];
    const manifestPath = path.join(DATA_DIR, 'manifest.json');
    if (!fs.existsSync(manifestPath)) {
        issues.push({
            severity: 'medium',
            type: 'data',
            file: 'data/manifest.json',
            description: 'manifest.json not found',
            expected: 'manifest.json with "generated" timestamp and file index',
            actual: 'File not found',
            recommendation: 'Run scripts/rebuild_manifest.py to generate the manifest.',
        });
        return issues;
    }
    const manifest = safeReadJson(manifestPath);
    if (!manifest) return issues;

    if (!manifest.generated) {
        issues.push({
            severity: 'medium',
            type: 'data',
            file: 'data/manifest.json',
            description: 'manifest.json is missing "generated" timestamp',
            expected: 'Top-level "generated" ISO-8601 UTC string',
            actual: 'Key absent',
            recommendation: 'Update rebuild_manifest.py to write a "generated" field.',
        });
    } else {
        const ageDays = (Date.now() - new Date(manifest.generated).getTime()) / (1000 * 60 * 60 * 24);
        if (ageDays > STALE_DATA_THRESHOLD_DAYS) {
            issues.push({
                severity: 'medium',
                type: 'data',
                file: 'data/manifest.json',
                description: `manifest.json generated ${Math.round(ageDays)} days ago (threshold: ${STALE_DATA_THRESHOLD_DAYS} days)`,
                expected: `Generated within the last ${STALE_DATA_THRESHOLD_DAYS} days`,
                actual: `${Math.round(ageDays)} days old`,
                recommendation: 'Run scripts/rebuild_manifest.py to refresh the manifest.',
            });
        }
    }

    const files = manifest.files || manifest.entries || [];
    const fileCount = Array.isArray(files) ? files.length : Object.keys(files).length;
    if (fileCount < 100) {
        issues.push({
            severity: 'medium',
            type: 'data',
            file: 'data/manifest.json',
            description: `manifest.json only lists ${fileCount} files (expected ≥ 100)`,
            expected: 'At least 100 data file entries',
            actual: `${fileCount} entries`,
            recommendation: 'Regenerate manifest after ensuring all data files are present.',
        });
    }
    return issues;
}

/**
 * Checks FRED data for blank series names and temporal gaps (Rules 6 & 7).
 * @returns {Array<object>} issues
 */
function checkFredData() {
    const issues = [];
    const fredPath = path.join(DATA_DIR, 'fred-data.json');
    if (!fs.existsSync(fredPath)) return issues;

    const data = safeReadJson(fredPath);
    if (!data) return issues;

    const seriesList = Array.isArray(data)
        ? data
        : Array.isArray(data.series)
            ? data.series
            : [];
    const MAX_GAP_DAYS = 35;

    for (const series of seriesList) {
        if (!series.name && !series.title) {
            issues.push({
                severity: 'high',
                type: 'data',
                file: 'data/fred-data.json',
                description: `FRED series missing "name" field (id: ${series.id || 'unknown'})`,
                expected: 'Non-empty "name" field',
                actual: 'name is blank or absent',
                recommendation: 'Reject series with blank names in the FRED fetch script.',
            });
        }

        const observations = series.observations || [];
        if (observations.length === 0) {
            issues.push({
                severity: 'high',
                type: 'data',
                file: 'data/fred-data.json',
                description: `FRED series "${series.name || series.id}" has zero observations`,
                expected: 'At least one observation',
                actual: '0 observations',
                recommendation: 'Alert and retry when series returns empty — do not store empty arrays.',
            });
            continue;
        }

        // Check for temporal gaps > MAX_GAP_DAYS
        const dates = observations
            .map(o => new Date(o.date))
            .filter(d => !isNaN(d))
            .sort((a, b) => a - b);
        for (let i = 1; i < dates.length; i++) {
            const gapDays = (dates[i] - dates[i - 1]) / (1000 * 60 * 60 * 24);
            if (gapDays > MAX_GAP_DAYS) {
                issues.push({
                    severity: 'medium',
                    type: 'data',
                    file: 'data/fred-data.json',
                    description: `FRED series "${series.name || series.id}" has a ${Math.round(gapDays)}-day gap between observations`,
                    expected: `No gap exceeding ${MAX_GAP_DAYS} days`,
                    actual: `${Math.round(gapDays)}-day gap detected`,
                    recommendation: 'Interpolate the missing observation and write it explicitly to the JSON.',
                });
                break; // Report first gap only per series
            }
        }
    }
    return issues;
}

/**
 * Checks CAR market report files for null required fields (Rule 8).
 * @returns {Array<object>} issues
 */
function checkCarMarketReports() {
    const issues = [];
    const REQUIRED_STATEWIDE = [
        'median_sale_price', 'active_listings', 'days_on_market',
        'new_listings', 'sold_listings', 'list_to_sale_ratio', 'months_supply',
    ];

    const carDir = path.join(DATA_DIR, 'market');
    if (!fs.existsSync(carDir)) return issues;

    const carFiles = fs.readdirSync(carDir).filter(f => /^car-market-report/.test(f) && f.endsWith('.json'));
    for (const file of carFiles) {
        const filePath = path.join(carDir, file);
        const data = safeReadJson(filePath);
        if (!data) continue;
        const relPath = `data/market/${file}`;

        for (const field of REQUIRED_STATEWIDE) {
            if (data[field] === null || data[field] === undefined) {
                issues.push({
                    severity: 'high',
                    type: 'data',
                    file: relPath,
                    description: `CAR report missing required statewide field: "${field}"`,
                    expected: `Non-null numeric value for "${field}"`,
                    actual: `${data[field]}`,
                    recommendation: 'Fix CAR PDF parser to fail loudly on missing required fields.',
                });
            }
        }
    }
    return issues;
}

/**
 * Runs all data integrity checks and returns a combined list of issues.
 * @returns {Promise<Array<object>>}
 */
async function runDataIntegrityChecks() {
    console.log('[data-integrity] Running data integrity checks...');
    const issues = [
        ...checkSentinelKeys(),
        ...checkFipsCodes(),
        ...checkCountyCoverage(),
        ...checkRequiredNumericFields(),
        ...checkProjectionYears(),
        ...checkManifest(),
        ...checkFredData(),
        ...checkCarMarketReports(),
    ];
    console.log(`[data-integrity] Found ${issues.length} issue(s).`);
    return issues;
}

module.exports = { runDataIntegrityChecks };
